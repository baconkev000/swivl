const SwitchingLogos = () => {
  const [index, setIndex] = useState(0);

  useEffect(() => {
    const timer = setInterval(() => {
      setIndex((prev) => (prev + 1) % brands.length);
    }, 2500);
    return () => clearInterval(timer);
  }, []);

  // Show a stack of 4 items
  const stack = [];
  for (let i = 0; i < 4; i++) {
    stack.push(brands[(index + i) % brands.length]);
  }

  return (
    <div className="relative w-24 h-24 mb-16 flex items-center justify-center">
      <AnimatePresence mode="popLayout">
        {stack.reverse().map((brand, i) => {
          const reverseIndex = 3 - i; // 0 for front, 1, 2, 3 for back
          return (
            <motion.div
              key={brand.name}
              initial={{ opacity: 0, y: 10, scale: 0.9 }}
              animate={{ 
                opacity: 1 - reverseIndex * 0.2, 
                y: -reverseIndex * 12,
                scale: 1 - reverseIndex * 0.05,
                zIndex: 10 - reverseIndex,
              }}
              exit={{ 
                opacity: 0, 
                y: 20, 
                scale: 1.1,
                zIndex: 20
              }}
              transition={{ 
                type: "spring",
                stiffness: 400,
                damping: 30,
                mass: 1
              }}
              className="absolute inset-0 rounded-[28px] flex items-center justify-center shadow-xl border border-white/10"
              style={{ backgroundColor: brand.color }}
            >
              <motion.div 
                className="scale-90"
                animate={{ opacity: reverseIndex === 0 ? 1 : 0.5 }}
              >
                {brand.logo}
              </motion.div>
            </motion.div>
          );
        })}
      </AnimatePresence>
    </div>
  );
};

export default SwitchingLogos;
